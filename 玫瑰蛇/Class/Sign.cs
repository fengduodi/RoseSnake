namespace 玫瑰蛇.Class
{
    public class Sign
    {
        public int x, y, forward_x, forward_y;

        public Sign(int x, int y, int forward_x, int forward_y)
        {
            this.x = x;
            this.y = y;

            this.forward_x = forward_x;
            this.forward_y = forward_y;
        }
    }
}